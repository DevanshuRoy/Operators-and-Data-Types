# hashtag can be used for single line comment

'''triple single quotes
for multi line comments'''

a=3 #int
print("a=",a," Datatype of a = ",type(a))

a=3.56 #float : fractional
print("a=",a," Datatype of a = ",type(a))

a=4e-2 #4 raised to power '-2' (e=exponent) : float : exponent
print("a=",a," Datatype of a = ",type(a))

a=2+4j #complex (j makes it complex data type)
print("a=",a," Datatype of a = ",type(a))

a="abc" #string
print("a=",a," Datatype of a = ",type(a))

a=True #boolean
print("a=",a," Datatype of a = ",type(a))

a=False #boolean
print("a=",a," Datatype of a = ",type(a))

a=None #nonetype
print("a=",a," Datatype of a = ",type(a))